public class Calcolatrice {
    int a ;
    int b;
    public Calcolatrice(int x, int y){
    a =x;
    b=y;
    }

    public int Somma (){
        return a + b;

    }
    public int Sottrazione (){
        return a - b;

    }
    public int Divisione (){
        return a / b;

    }
    public int Moltiplicazione (){
        return a * b;

    }
}
